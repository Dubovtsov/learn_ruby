####################################################
# Вывод гласных с их порядковым номером в алфавите #
####################################################

vowels = ["а", "о", "э", "ы", "у", "я", "е", "ю", "и"]

abc = Hash[("а".."я").to_a.zip((1..32).to_a)]

vowels.each do |letter|
  puts abc.select {|k,v| k == letter}
end
